const resetBtn = document.getElementById('resetJourney');
const progressText = document.getElementById('progressText');

// Milestone positions (matching CSS)
const milestonePositions = [
    { left: '8%', top: '70px' },
    { left: '33%', top: '220px' },
    { left: '50%', top: '370px' },
    { left: '58%', top: '620px' },
    { left: '42%', top: '820px' },
    { left: '33%', top: '1070px' },
    { left: '58%', top: '1270px' },
    { left: '67%', top: '1520px' },
    { left: '50%', top: '1720px' }
];

// Initialize
function init() {
    // Start with avatar paused
    avatarContainer.classList.add('paused');
    
    // Add click listeners to milestones
    for (let i = 1; i <= 9; i++) {
        const milestone = document.getElementById(`milestone-${i}`);
        milestone.addEventListener('click', () => openMilestone(i));
    }
    
    // Modal close and next
    modalClose.addEventListener('click', closeModal);
    modalNext.addEventListener('click', handleNextClick);
    modal.addEventListener('click', (e) => {
        if (e.target === modal) closeModal();
    });
    
    // Goodbye modal close button
    const goodbyeCloseBtn = document.getElementById('goodbyeClose');
    if (goodbyeCloseBtn) {
        goodbyeCloseBtn.addEventListener('click', closeGoodbyeModal);
    }
    
    // Control buttons
    startBtn.addEventListener('click', startJourney);
    resetBtn.addEventListener('click', resetJourney);
    
    // Keyboard navigation
    document.addEventListener('keydown', handleKeyPress);
}

// Start the journey animation
function startJourney() {
    if (isAnimating) return;
    
    if (currentMilestone === 0) {
        moveToNextMilestone();
    } else if (currentMilestone < 9) {
        moveToNextMilestone();
    }
}

// Move avatar to next milestone
function moveToNextMilestone() {
    if (isAnimating || currentMilestone >= 9) return;
    
    isAnimating = true;
    currentMilestone++;
    
    // Remove paused state to start walking animation
    avatarContainer.classList.remove('paused');
    
    const position = milestonePositions[currentMilestone - 1];
    
    // Scroll to center the milestone in viewport FIRST - before setting avatar position
    const milestoneTop = parseInt(position.top);
    const viewportHeight = window.innerHeight;
    const scrollTarget = milestoneTop - (viewportHeight / 2) + 50; // 50px offset for milestone height
    
    // Start scroll immediately using native smooth scroll
    window.scrollTo({
        top: Math.max(0, scrollTarget),
        behavior: 'smooth'
    });
    
    // Then set avatar position to start walking
    avatarContainer.style.left = position.left;
    avatarContainer.style.top = position.top;
    
    // Mark milestone as active
    const milestone = document.getElementById(`milestone-${currentMilestone}`);
    milestone.classList.add('active');
    
    // Update progress
    updateProgress();
    
    // After animation, pause walking and open modal (increased to match new duration)
    setTimeout(() => {
        // Pause walking animation
        avatarContainer.classList.add('paused');
        
        milestone.classList.remove('active');
        milestone.classList.add('visited');
        visitedMilestones.add(currentMilestone);
        isAnimating = false;
        
        // Open milestone modal
        openMilestone(currentMilestone);
    }, 2000); // Match the CSS transition duration (2.0s)
}

// Open milestone modal
function openMilestone(milestoneNum) {
    const content = milestoneContent[milestoneNum];
    if (!content) return;
    
    modalBody.innerHTML = content.content;
    modal.classList.add('show');
    
    // Scroll modal content to top
    modalBody.scrollTop = 0;
    
    // Show/hide Next button based on milestone
    if (milestoneNum === 9) {
        modalNext.style.display = 'none';
    } else {
        modalNext.style.display = 'flex';
    }
    
    // Mark as visited
    visitedMilestones.add(milestoneNum);
    const milestone = document.getElementById(`milestone-${milestoneNum}`);
    milestone.classList.add('visited');
    
    updateProgress();
}

// End Journey function - triggered by button in Milestone 9
function endJourney() {
    // Close the milestone modal first
    closeModal();
    
    // Trigger confetti celebration
    setTimeout(() => {
        celebrateCompletion();
        // Show goodbye modal after confetti
        setTimeout(() => {
            showGoodbyeModal();
        }, 2000);
    }, 300);
}

// Show goodbye modal
function showGoodbyeModal() {
    const goodbyeModal = document.getElementById('goodbyeModal');
    goodbyeModal.classList.add('show');
}

// Close goodbye modal
function closeGoodbyeModal() {
    const goodbyeModal = document.getElementById('goodbyeModal');
    goodbyeModal.classList.remove('show');
}

// Close modal
function closeModal() {
    modal.classList.remove('show');
}

// Handle Next button click
function handleNextClick() {
    closeModal();
    // Small delay before moving to next milestone
    setTimeout(() => {
        if (currentMilestone < 9) {
            moveToNextMilestone();
        }
    }, 300);
}

// Reset journey
function resetJourney() {
    currentMilestone = 0;
    visitedMilestones.clear();
    isAnimating = false;
    
    // Reset avatar position and pause animation
    avatarContainer.style.left = '8%';
    avatarContainer.style.top = '70px';
    avatarContainer.classList.add('paused');
    
    // Reset all milestones
    for (let i = 1; i <= 9; i++) {
        const milestone = document.getElementById(`milestone-${i}`);
        milestone.classList.remove('visited', 'active');
    }
    
    updateProgress();
    closeModal();
}

// Update progress indicator
function updateProgress() {
    const visited = visitedMilestones.size;
    if (visited === 0) {
        progressText.textContent = 'Ready to begin';
        startBtn.innerHTML = '▶ Start Journey';
    } else if (visited === 9) {
        progressText.textContent = '🎉 Journey Complete!';
        startBtn.innerHTML = '▶ Start Journey';
    } else {
        progressText.textContent = `${visited}/9 milestones explored`;
        startBtn.innerHTML = '▶ Continue Journey';
    }
}

// Keyboard navigation
function handleKeyPress(e) {
    if (modal.classList.contains('show')) {
        if (e.key === 'Escape') {
            closeModal();
        }
        return;
    }
    
    if (e.key === 'ArrowRight' || e.key === ' ') {
        e.preventDefault();
        moveToNextMilestone();
    } else if (e.key === 'r' || e.key === 'R') {
        resetJourney();
    }
}

// Carousel functionality
let currentSlide = 0;

function moveCarousel(direction) {
    const slides = document.querySelectorAll('.carousel-slide');
    const dots = document.querySelectorAll('.dot');
    
    if (slides.length === 0) return;
    
    // Remove active class from current slide and dot
    slides[currentSlide].classList.remove('active');
    dots[currentSlide].classList.remove('active');
    
    // Calculate new slide index
    currentSlide = (currentSlide + direction + slides.length) % slides.length;
    
    // Add active class to new slide and dot
    slides[currentSlide].classList.add('active');
    dots[currentSlide].classList.add('active');
}

function goToSlide(index) {
    const slides = document.querySelectorAll('.carousel-slide');
    const dots = document.querySelectorAll('.dot');
    
    if (slides.length === 0) return;
    
    // Remove active class from current slide and dot
    slides[currentSlide].classList.remove('active');
    dots[currentSlide].classList.remove('active');
    
    // Set new slide index
    currentSlide = index;
    
    // Add active class to new slide and dot
    slides[currentSlide].classList.add('active');
    dots[currentSlide].classList.add('active');
}

// Confetti celebration
function celebrateCompletion() {
    // Fire confetti from multiple angles
    const duration = 3000;
    const animationEnd = Date.now() + duration;
    const defaults = { startVelocity: 30, spread: 360, ticks: 60, zIndex: 2000 };

    function randomInRange(min, max) {
        return Math.random() * (max - min) + min;
    }

    const interval = setInterval(function() {
        const timeLeft = animationEnd - Date.now();

        if (timeLeft <= 0) {
            return clearInterval(interval);
        }

        const particleCount = 50 * (timeLeft / duration);
        
        // Fire from left side
        confetti({
            ...defaults,
            particleCount,
            origin: { x: randomInRange(0.1, 0.3), y: Math.random() - 0.2 },
            colors: ['#0f62fe', '#0043ce', '#42be65', '#24a148', '#ffffff']
        });
        
        // Fire from right side
        confetti({
            ...defaults,
            particleCount,
            origin: { x: randomInRange(0.7, 0.9), y: Math.random() - 0.2 },
            colors: ['#0f62fe', '#0043ce', '#42be65', '#24a148', '#ffffff']
        });
    }, 250);
}

// Initialize on load
document.addEventListener('DOMContentLoaded', init);

